#include "libft.h"

int	ft_atoi(const char *str)
{
	int signe;
	int nbf;
	int i;

	i = 0;
	nbf = 0;
	signe = 0;
	while ((str[i] > 8 && str[i] < 14) || str[i] == 32)
		i++;
	if (str[i] == '-')
	{
		signe++;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbf = nbf * 10 + str[i] - '0';
		i++;
	}
	if (signe == 1)
		return (-nbf);
	return (nbf);
}
