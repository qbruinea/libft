#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*tab;

	i = 0;
	if (!(tab = (char *)malloc(sizeof(*tab) * (ft_strlen(s1) + 1))))
		return (0);
	while (s1[i])
	{
		tab[i] = s1[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
